<?php

function sum($n1,$n2) {
    $result = $n1+$n2;
    return $result;
}

function subtract($n1,$n2) {
    $result = $n1-$n2;
    return $result;
}

function multiply($n1,$n2) {
    $result = $n1*$n2;
    return $result;
}

function divide($n1,$n2) {
    $result = $n1/$n2;
    return $result;
}

function modulus($n1,$n2) {
    $result = $n1%$n2;
    return $result;
}

